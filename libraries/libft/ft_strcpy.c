#include "libft.h"

void    ft_strcpy(char *str1, const char *str2)
{
    int i;

    if (!str2)
        return ;
    i = 0;
    while (str2[i])
    {
        str1[i] = str2[i];
        i++;
    }
    str1[i] = '\0';
}